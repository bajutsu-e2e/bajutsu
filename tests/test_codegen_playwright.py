"""Tests for Playwright code generation (structural scenario -> TypeScript)."""

from __future__ import annotations

from pathlib import Path

from typer.testing import CliRunner

from bajutsu.cli import app
from bajutsu.codegen_playwright import describe_name_for, to_playwright
from bajutsu.scenario import load_scenarios

BASE = "http://localhost:3000"
runner = CliRunner()


def _gen(yaml: str, env: dict[str, str] | None = None, base_url: str = BASE) -> str:
    scenarios = load_scenarios(yaml)
    return to_playwright(scenarios, "Fixture", base_url, env or {})


def test_header_describe_and_base_url() -> None:
    code = _gen("- name: x\n  steps:\n    - tap: { id: a }\n")
    assert "import { test, expect } from '@playwright/test';" in code
    assert f"const BASE_URL = '{BASE}';" in code
    assert "test.describe('Fixture', () => {" in code
    assert "// Generated by bajutsu" in code


